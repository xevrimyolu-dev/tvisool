function setupFilelistManagement(input, list, array) {
    input.addEventListener('change', (e) => {
        Array.from(e.target.files).forEach(file => {
            if (!array.some(f => f.name === file.name)) {
                array.push(file);
                const li = document.createElement('li');
                li.dataset.fileName = file.name;
                li.innerHTML = `<span>${file.name}</span><i class="fas fa-trash-alt delete-icon"></i>`;
                list.appendChild(li);
            }
        });
        e.target.value = '';
    });
    list.addEventListener('click', (e) => {
        if (e.target.classList.contains('delete-icon')) {
            const li = e.target.parentElement;
            const name = li.dataset.fileName;
            const index = array.findIndex(f => f.name === name);
            if (index > -1) array.splice(index, 1);
            li.remove();
        }
    });
}
