async function handleBridgeBlobResponse(response, statusDiv, originalFileName, fileType) {
    const langDict = translations[localStorage.getItem('userLanguage') || 'en'];
    statusDiv.innerHTML = '';

    const reportToAdminPanel = async (status, sizeMB = 0) => {
        try {
            await fetch('/log_local_action', {
                method: 'POST',
                headers: {'Content-Type': 'application/json', 'X-CSRFToken': getCSRFToken()},
                body: JSON.stringify({
                    action_type: `${fileType} İşlemi (Yerel Köprü)`,
                    file_name: originalFileName,
                    file_size_mb: sizeMB,
                    status: status
                })
            });
        } catch (err) {}
    };

    if (response.ok) {
        const contentType = response.headers.get("content-type");

        // Başarı Mesajını Belirle
        let successTitle = langDict['success_unpack']; // Varsayılan
        if (fileType === 'OBB') {
            // Eğer işlem Repack ise (dosya adından anla) veya Unpack ise
            if (originalFileName.includes('repack')) successTitle = langDict['success_repack_obb'];
            else successTitle = langDict['success_unpack_obb'];
        } else if (fileType === 'PAK') {
            // PAK için özel mesajlar
            // Not: Repack işleminde fileType 'PAK' gelir, dosya adı veya bağlamdan repack olduğu anlaşılmalı.
            // Ancak handleBridgeBlobResponse şu an basit tutuluyor.
            // Unpack işlemi genelde bu fonksiyonu kullanır. Repack ayrı bir blokta.
            successTitle = langDict['success_unpack_pak'] || 'PAK Unpack Başarılı!';
        }

        // Android (JSON) Yanıtı
        if (contentType && contentType.includes("application/json")) {
            const data = await response.json();
            const savedMessage = langDict['success_android_saved'];

            const successBox = document.createElement('div');
            successBox.className = 'status-success-box';
            successBox.style.cssText = 'background-color: #d4edda; color: #155724; padding: 15px; border-radius: 5px; margin-top: 10px; border: 1px solid #c3e6cb; word-break: break-all;';
            
            successBox.innerHTML = `
                <strong>✅ ${successTitle}</strong><br><br>
                ${savedMessage}<br>
                <code>${data.saved_path || 'Download Klasörü'}</code>
            `;
            statusDiv.appendChild(successBox);
            await reportToAdminPanel('Başarılı (Android)', data.file_size_mb || 0);
        } 
        // PC (Blob) Yanıtı
        else {
            const blob = await response.blob();
            const sizeMB = blob.size / (1024 * 1024);
            
            // Dosya adı belirleme
            let filename = 'downloaded_file';
            
            // Orijinal dosya adına göre yeni dosya adı oluştur
            if (fileType === 'OBB') {
                if (originalFileName.includes('repack')) {
                    filename = originalFileName.replace('_repacked', '') || 'repacked.obb';
                } else {
                    filename = originalFileName.replace('.obb', '_unpacked.zip') || 'unpacked.zip';
                }
            } else if (fileType === 'PAK') {
                filename = originalFileName.replace('.pak', '_unpacked.zip') || 'unpacked.zip';
            }
            
            // Content-Disposition header'ı öncelikli
            const disposition = response.headers.get('content-disposition');
            if (disposition && disposition.includes('attachment')) {
                const matches = /filename[^;=\n]*=((['"]).*?\2|[^;\n]*)/.exec(disposition);
                if (matches != null && matches[1]) {
                    filename = matches[1].replace(/['"]/g, '');
                }
            }

            // İndirmeyi tetikle
            const url = window.URL.createObjectURL(blob);
            const a = document.createElement('a');
            a.style.display = 'none';
            a.href = url;
            a.download = filename;
            document.body.appendChild(a);
            a.click();
            window.URL.revokeObjectURL(url);
            a.remove();

            const messageP = document.createElement('p');
            messageP.className = 'status-success';
            messageP.textContent = successTitle;
            statusDiv.appendChild(messageP);

            await reportToAdminPanel('Başarılı (PC/EXE)', sizeMB);
        }
        fetchUserRoleAndLimits();
    } else {
        // Hata Durumu
        await reportToAdminPanel('Hata/Başarısız');
        try {
            const errorData = await response.json();
            statusDiv.innerHTML = `<p class="status-error">${langDict['error_prefix']} ${errorData.error?.message || 'Hata'}</p>`;
        } catch (e) {
            statusDiv.innerHTML = `<p class="status-error">${langDict['error_prefix']} Yerel köprü hatası.</p>`;
        }
    }
}

// --- processFileUpload Fonksiyonunu Güncelliyoruz (Unpack PAK Limit Kontrolü Eklendi) ---
async function processFileUpload(button, fileInput, defaultUrl, formDataKey) {
    if (!fileInput.files.length) return;
    
    const file = fileInput.files[0];
    const statusDiv = button.closest('.page').querySelector('.status-container');
    const langDict = translations[localStorage.getItem('userLanguage') || 'en'];
    
    const isObbUnpack = defaultUrl === '/unpack_obb';
    const isPakUnpack = defaultUrl === '/unpack';
    
    // Bakım Kontrolü
    const parentPage = button.closest('.page');
    if (parentPage && parentPage.dataset.maintenance === 'true') {
        return;
    }

    // --- LIMIT KONTROLLERİ ---
    const fileSize = file.size;

    // 1. OBB Unpack Kontrolü
    if (isObbUnpack) {
        const limit = userByteLimits.obb_max_size;
        if (fileSize > limit) {
            const limitStr = (limit / 1024 / 1024 / 1024) >= 1 ? 
                             `${(limit / 1024 / 1024 / 1024).toFixed(1)} GB` : 
                             `${(limit / 1024 / 1024).toFixed(0)} MB`;
            const errorTemplate = langDict['error_obb_size_limit_role'] || 'File size exceeds {limit} limit.';
            statusDiv.innerHTML = `<p class="status-error">${langDict['error_prefix']} ${errorTemplate.replace('{limit}', limitStr)}</p>`;
            
            // Loglama (Admin Paneli için)
            logFrontendFailure('unpack_obb_fail', file.name, fileSize, limit);
            return;
        }
    }

    // 2. PAK Unpack Kontrolü (YENİ EKLENDİ)
    if (isPakUnpack) {
        const limit = userByteLimits.pak_max_size;
        if (fileSize > limit) {
            const limitStr = (limit / 1024 / 1024 / 1024) >= 1 ? 
                             `${(limit / 1024 / 1024 / 1024).toFixed(1)} GB` : 
                             `${(limit / 1024 / 1024).toFixed(0)} MB`;
            const errorTemplate = langDict['error_pak_size_limit_role'] || 'File size exceeds {limit} limit.';
            statusDiv.innerHTML = `<p class="status-error">${langDict['error_prefix']} ${errorTemplate.replace('{limit}', limitStr)}</p>`;
            
            // Loglama (Admin Paneli için)
            logFrontendFailure('unpack_pak_fail', file.name, fileSize, limit);
            return;
        }
    }
    
    let targetUrl;
    let headers = {};
    let fileType = 'FILE';

    // Yerel Köprü Kontrolü (Hem OBB hem PAK için)
    await checkLocalBridgeStatus();

    if (isLocalBridgeAvailable) {
        // *** KÖPRÜ MODU (ANDROID/PC YEREL) ***
        if (isObbUnpack) {
            targetUrl = `${LOCAL_BRIDGE_BASE_URL}/local/unpack_obb`;
            fileType = 'OBB';
        } else if (isPakUnpack) {
            targetUrl = `${LOCAL_BRIDGE_BASE_URL}/local/unpack_pak`;
            fileType = 'PAK';
        } else {
            // Diğer işlemler için varsayılan (Server)
            targetUrl = defaultUrl;
            headers['X-CSRFToken'] = getCSRFToken();
        }

        if (targetUrl.includes('/local/')) {
            headers['X-Local-Auth-Token'] = localAuthToken;
        }
    } else {
        // *** SUNUCU MODU (CLOUD) ***
        targetUrl = defaultUrl;
        headers['X-CSRFToken'] = getCSRFToken();
    }

    const formData = new FormData();
    formData.append(formDataKey, file); 
    
    button.disabled = true;
    statusDiv.innerHTML = `<p class="status-processing">${langDict['processing']}</p>`;

    try {
        const response = await fetch(targetUrl, {
            method: 'POST',
            headers: headers,
            body: formData
        });
        
        // Eğer yerel işlemse ve başarılıysa
        if (isLocalBridgeAvailable && (isObbUnpack || isPakUnpack) && response.ok) {
            await handleBridgeBlobResponse(response, statusDiv, file.name, fileType);
        } else {
            await handleApiResponse(response, statusDiv);
        }

    } catch (error) {
        let errorMessage = langDict['error_prefix'] + ' Ağ hatası.';
        if (isLocalBridgeAvailable && (isObbUnpack || isPakUnpack)) {
            errorMessage = langDict['error_prefix'] + ' Yerel işlem başarısız oldu.';
        }
        statusDiv.innerHTML = `<p class="status-error">${errorMessage}</p>`;
    } finally {
        button.disabled = false;
    }
}

