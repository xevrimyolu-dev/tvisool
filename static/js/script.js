    function startRoleExpiryCountdown(durationSeconds, displayElement) {
        if (roleExpiryCountdownInterval) clearInterval(roleExpiryCountdownInterval);

        let timer = Math.floor(durationSeconds);
        const lang = localStorage.getItem('userLanguage') || 'en';

        const updateCounter = () => {
            if (timer <= 0) {
                clearInterval(roleExpiryCountdownInterval);
                displayElement.textContent = translations[lang]['role_expiry_expired'] || 'Süre Doldu';
                setTimeout(fetchUserRoleAndLimits, 1500);
                return;
            }

            const hours = Math.floor(timer / 3600);
            const minutes = Math.floor((timer % 3600) / 60);
            const seconds = timer % 60;

            const formattedTime = [
                hours.toString().padStart(2, '0'),
                minutes.toString().padStart(2, '0'),
                seconds.toString().padStart(2, '0')
            ].join(':');

            displayElement.textContent = formattedTime;
            timer--;
        };

        updateCounter();
        roleExpiryCountdownInterval = setInterval(updateCounter, 1000);
    }

async function fetchUserRoleAndLimits() {
    try {
        const response = await fetch('/get_user_limits');
        if (!response.ok) throw new Error('Sunucu yanıt vermiyor.');
        const data = await response.json();
        
        // === YENİ EKLEME: Byte limitlerini global değişkene kaydet ===
        if (data.byte_limits) {
            userByteLimits = data.byte_limits;
        }
        // === EKLEME SONU ===

        if (!data || !data.role) {
             if (document.getElementById('userRoleText')) document.getElementById('userRoleText').textContent = 'HATA';
             return;
        }

        const langDict = translations[localStorage.getItem('userLanguage') || 'en'];

        let roleText = data.role.toUpperCase();
        const roleKey = `role_${data.role.replace('_', '')}`;
        if (langDict[roleKey]) {
            roleText = langDict[roleKey];
        } else if (data.role === 'ücretsiz' && langDict['role_free']) {
            roleText = langDict['role_free'];
        }

        let totalText = data.total;
        if (totalText === 'Sınırsız') { totalText = langDict['unlimited_text']; }
        let remainingText = data.remaining;
        if (remainingText === 'Sınırsız') { remainingText = langDict['unlimited_text']; }
        let actionsText = data.current_actions;
        if (actionsText === 'Yok') { actionsText = langDict['na_text']; }

        const cdData = data.cooldown_data;
        const tooltipCooldownElement = document.getElementById('tooltipCooldownTime');
        if (tooltipCountdownInterval) clearInterval(tooltipCountdownInterval);

        if (cdData && cdData.key === 'cooldown_active') {
            const timeParts = cdData.time.split(':').map(Number);
            const totalSeconds = timeParts[0] * 3600 + timeParts[1] * 60 + timeParts[2];
            startTooltipCountdown(totalSeconds, tooltipCooldownElement, `${langDict['cooldown_active_prefix']} `);
        } else {
            let cooldownText = '';
            if(cdData) {
                switch (cdData.key) {
                    case 'never_used': cooldownText = langDict['cooldown_never_used']; break;
                    case 'refreshed': cooldownText = langDict['cooldown_refreshed']; break;
                    case 'unlimited': cooldownText = langDict['unlimited_text']; break;
                    default: cooldownText = cdData.key || 'N/A';
                }
            }
            if (tooltipCooldownElement) tooltipCooldownElement.textContent = cooldownText;
        }

        const expiryContainer = document.getElementById('expiryContainer');
        const expiryElement = document.getElementById('tooltipRoleExpiry');

        if (roleExpiryCountdownInterval) clearInterval(roleExpiryCountdownInterval);

        if (expiryContainer && expiryElement) {
            const userRole = data.role;

            if (userRole === 'ücretsiz') {
                expiryContainer.style.display = 'flex';
                expiryElement.textContent = langDict['upgrade_your_role'] || 'Rolünüzü Yükseltin!';

            } else if (userRole === 'premium' || userRole === 'dev') {
                expiryContainer.style.display = 'flex';

                if (!data.role_expiry_date) {
                    expiryElement.textContent = langDict['role_expiry_permanent'] || 'Süresiz';
                } else {
                    const expiryDate = new Date(data.role_expiry_date);
                    const now = new Date();
                    const diffMilliseconds = expiryDate - now;

                    if (diffMilliseconds <= 0) {
                        expiryElement.textContent = langDict['role_expiry_expired'] || 'Süre Doldu';
                    } else if (diffMilliseconds < 24 * 60 * 60 * 1000) {
                        startRoleExpiryCountdown(diffMilliseconds / 1000, expiryElement);
                    } else {
                        const daysLeft = Math.ceil(diffMilliseconds / (1000 * 60 * 60 * 24));
                        expiryElement.textContent = (langDict['role_expiry_days_left'] || '{days} gün').replace('{days}', daysLeft);
                    }
                }
            } else {
                expiryContainer.style.display = 'none';
            }
        }

        if (document.getElementById('userRoleText')) document.getElementById('userRoleText').textContent = roleText;
        if (document.getElementById('tooltipDailyLimit')) document.getElementById('tooltipDailyLimit').textContent = totalText;
        if (document.getElementById('tooltipActionsTaken')) document.getElementById('tooltipActionsTaken').textContent = actionsText;
        if (document.getElementById('tooltipRemainingActions')) document.getElementById('tooltipRemainingActions').textContent = remainingText;

        if(userRoleButton) {
            userRoleButton.className = 'user-role-badge';
            userRoleButton.classList.add(`role-${data.role.replace('_', '-')}`);
        }
    } catch (error) {
        if (document.getElementById('userRoleText')) document.getElementById('userRoleText').textContent = "N/A";
    }
}

