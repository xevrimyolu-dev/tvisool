/**
 * Global çeviri yardımcısı fonksiyonu.
 * Bir anahtar ve isteğe bağlı parametreler alarak çevrilmiş metni döndürür.
 * @param {string} key - Çeviri anahtarı (örn: 'post_load_error').
 * @param {object} [params={}] - Metin içindeki değişkenleri değiştirmek için bir nesne (örn: {count: 5}).
 * @returns {string} Çevrilmiş veya orijinal metin.
 */
function getLang(key, params = {}) {
    // 1. Kayıtlı dili veya varsayılan olarak 'en' (İngilizce) dilini al
    const lang = localStorage.getItem('userLanguage') || 'en';

    let translationString = '';

    // 2. Önce mevcut dilde çeviriyi ara
    if (translations[lang] && translations[lang][key]) {
        translationString = translations[lang][key];
    } 
    // 3. Bulamazsa, varsayılan dil olan İngilizce'de ara
    else if (translations['en'] && translations['en'][key]) {
        translationString = translations['en'][key];
    } 
    // 4. Hiçbir yerde bulamazsa, anahtarın kendisini döndür
    else {
        return key; // Anahtarı döndürmeye devam et
    }

    // 5. Metin içindeki {değişken} alanlarını parametrelerle değiştir
    if (params) {
        for (const [paramKey, paramValue] of Object.entries(params)) {
            // new RegExp() kullanarak metindeki tüm {paramKey} örneklerini değiştir
            translationString = translationString.replace(new RegExp(`{${paramKey}}`, 'g'), paramValue);
        }
    }

    return translationString;
}

/**
 * Ekranda kısa süreli bir bildirim (toast) gösterir.
 * @param {string} message Gösterilecek mesaj.
 * @param {string} [type='info'] Bildirim türü ('success' veya 'error').
 */
function showToast(message, type = 'info') {
  // 1. Toast elementini oluştur
  const toast = document.createElement('div');
  toast.className = 'toast-notification';
  toast.textContent = message;

  // 2. Türüne göre stil sınıfını ekle
  if (type === 'error') {
    toast.classList.add('toast-error');
  } else if (type === 'success') {
    toast.classList.add('toast-success');
  }

  // 3. Sayfaya ekle
  document.body.appendChild(toast);

  // 4. Görünür yap (kısa bir gecikmeyle animasyon için)
  setTimeout(() => {
    toast.classList.add('show');
  }, 100); // 100ms gecikme

  // 5. 3 saniye sonra gizle
  setTimeout(() => {
    toast.classList.remove('show');
  }, 3000); // 3 saniye görünür kal

  // 6. Animasyon bittikten sonra DOM'dan kaldır
  setTimeout(() => {
    if (toast.parentNode === document.body) {
      document.body.removeChild(toast);
    }
  }, 3500); // 3.5 saniye (animasyon süresi dahil)
}

