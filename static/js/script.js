    function formatTime(seconds) {
        let h = Math.floor(seconds / 3600);
        let m = Math.floor((seconds % 3600) / 60);
        let s = Math.floor(seconds % 60);
        h = h < 10 ? '0' + h : h;
        m = m < 10 ? '0' + m : m;
        s = s < 10 ? '0' + s : s;
        return `${h}:${m}:${s}`;
    }

    function startStatusCountdown(duration, displayElement, messageTemplate) {
    if (statusCountdownInterval) clearInterval(statusCountdownInterval);

    let timer = duration;

    const updateDisplay = () => {
        // Önceki içeriği temizle
        while (displayElement.firstChild) {
            displayElement.removeChild(displayElement.firstChild);
        }

        const timeString = formatTime(timer);
        const fullMessage = messageTemplate.replace('{time}', timeString);

        // Güvenli şekilde element oluştur
        const p = document.createElement('p');
        p.className = 'status-error';
        p.textContent = fullMessage; // XSS koruması - textContent kullanıyoruz
        displayElement.appendChild(p);

        if (--timer < 0) {
            clearInterval(statusCountdownInterval);

            // Süre dolunca içeriği temizle
            while (displayElement.firstChild) {
                displayElement.removeChild(displayElement.firstChild);
            }

            fetchUserRoleAndLimits();
        }
    };

    // Hemen ilk güncellemeyi yap
    updateDisplay();

    // Sonra saniyede bir güncelle
    statusCountdownInterval = setInterval(updateDisplay, 1000);
    }

