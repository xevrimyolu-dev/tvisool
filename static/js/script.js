function changeLanguage(lang) {
    if (!translations[lang]) lang = 'en';
    localStorage.setItem('userLanguage', lang);
    if (typeof updateServerLanguage === 'function') {
        updateServerLanguage(lang);
    }

    // YENİ: Kullanıcı adını global olarak al
    const globalUsername = document.body.dataset.username || 'user';

    document.querySelectorAll('[data-lang-key]').forEach(elem => {
        const key = elem.getAttribute('data-lang-key');

        if (translations[lang] && translations[lang][key]) {
            let translatedText = translations[lang][key];

            if (elem.dataset.username) {
                translatedText = translatedText.replace('{username}', elem.dataset.username);
            }
            // GÜNCELLEME: Global kullanıcı adını kullan
            else if (translatedText.includes('{username}')) {
                 translatedText = translatedText.replace('{username}', globalUsername);
            }

            if (elem.tagName !== 'INPUT' && elem.tagName !== 'TEXTAREA') {
                elem.textContent = translatedText;
            }
        }

        // GÜNCELLEME: Placeholder'lar için getLang kullanarak parametre desteği ekle
        if (elem.placeholder !== undefined) {
             const placeholderKey = elem.getAttribute('data-placeholder-key') || key;
             
             // Parametreleri hazırla
             let params = {};
             if (elem.dataset.username) {
                 params.username = elem.dataset.username;
             } else {
                 params.username = globalUsername;
             }

             // Çeviriyi getLang ile al
             const translatedPlaceholder = getLang(placeholderKey, params);
             
             if (translatedPlaceholder !== placeholderKey) {
                 elem.placeholder = translatedPlaceholder;
             }
             // Ana anahtarı da kontrol et
             else if (key !== placeholderKey) {
                 const fallbackPlaceholder = getLang(key, params);
                 if (fallbackPlaceholder !== key) {
                    elem.placeholder = fallbackPlaceholder;
                 }
             }
        }
    });

    const langCurrentBtn = document.querySelector('.lang-current');
    if(langCurrentBtn) {
        langCurrentBtn.querySelector('img').src = `/static/images/${lang}.png`;
        const langNativeName = (translations[lang] && translations[lang]['lang_native']) || lang.toUpperCase();
        langCurrentBtn.querySelector('span').textContent = langNativeName;
    }

    if (typeof fetchUserRoleAndLimits === 'function') {
        fetchUserRoleAndLimits();
    }

    document.dispatchEvent(new CustomEvent('languageChanged', { detail: { newLang: lang } }));
}

